# Создаем переменную для количества выполненных ДЗ
number_of_completed = 12

# Создаем переменную для количества затраченных часов
number_of_hours_spent = 1.5

# Создаем переменную для названия курса
course_name = 'Python'

# Создаем переменную и вычисляем время на одно задание
time_for_one_task = number_of_hours_spent / number_of_completed

# Выводим на консоль
print("Курс: ", course_name, ", всего задач:", number_of_completed, ", затрачено часов:", number_of_hours_spent, ", среднее время выполнения ", time_for_one_task, " часа.")
